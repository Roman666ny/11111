def sum_of_digits(n):
    # Базовый случай: если число состоит из одной цифры
    if n < 10:
        return n
    # Рекурсивный случай: последняя цифра + сумма остальных цифр
    return n % 10 + sum_of_digits(n // 10)

# Тестирование
num = 12345
print(f"Сумма цифр числа {num} = {sum_of_digits(num)}")

# Вывод: Сумма цифр числа 12345 = 15
